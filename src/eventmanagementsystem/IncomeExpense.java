    public ArrayList<IncomeExpenseHandler> IEList(){
        ArrayList<IncomeExpenseHandler> ieList=new ArrayList<>();
        sqlConnect co = new sqlConnect();
        co.connectDB();
        String query="Select *from IncomeExpense";
        
        try {
            
            co.resultSet = co.statement.executeQuery(query);
            IncomeExpenseHandler ie;

            while (co.resultSet.next()) {
                ie=new IncomeExpenseHandler(co.resultSet.getInt("Entry_No"),co.resultSet.getString("Entry_type"),co.resultSet.getString("Entry_category")
                ,co.resultSet.getString("Entry_description"),co.resultSet.getInt("Emp_ID"),co.resultSet.getInt("Bill_No"),co.resultSet.getString("Entry_date"),co.resultSet.getInt("totalCash"));
                ieList.add(ie);
                
              
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

        return ieList;
    }
    public void show_ie()
    {
        ArrayList<IncomeExpenseHandler>list=IEList();
        DefaultTableModel model = (DefaultTableModel) emp_info_table.getModel();
        model.setRowCount(0);
        Object[] row =new Object[9];
        for(int i=0;i<list.size();i++)
        {
            row[0]=list.get(i).getEntry_No();
            row[1]=list.get(i).getEntry_type();
            row[2]=list.get(i).getEntry_category();
            row[3]=list.get(i).getEntry_description();
            row[4]=list.get(i).getEmp_ID();
            row[5]=list.get(i).getBill_No();
            row[6]=list.get(i).getEntry_date();
            row[7]=list.get(i).getTotalCash();

            model.addRow(row);
            
        }
        
    }
    

