    public RestaurantFoodList(String res_id) {
        initComponents();
        this.Res_id=res_id;
        
        sqlConnect co = new sqlConnect();
        co.connectDB();
        
        try {
                           
            query = "select Res_Name from Restaurant where Res_ID = "+Res_id;
            co.resultSet = co.statement.executeQuery(query);

            while(co.resultSet.next()) {
                jLabel1.setText((co.resultSet.getString("Res_Name")) + " Food List");
            }
            
        }catch(Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(this, "Connection Failed!");
        }
        
        
        
        try {
                           
            query = "select * from foodPriceList where Res_ID = "+Res_id;
            co.resultSet = co.statement.executeQuery(query);

            if(co.resultSet.next()) {
                f1.setText(co.resultSet.getString("fooditem1"));
                f2.setText(co.resultSet.getString("fooditem2"));
                f3.setText(co.resultSet.getString("fooditem3"));
                f4.setText(co.resultSet.getString("fooditem4"));
                f5.setText(co.resultSet.getString("fooditem5"));
                p1.setText(co.resultSet.getString("RiceP"));
                p2.setText(co.resultSet.getString("RoastP"));
                p3.setText(co.resultSet.getString("KachhiP"));
                p4.setText(co.resultSet.getString("BeefP"));
                p5.setText(co.resultSet.getString("drinksP")); 
            } 
            
            else{
                JOptionPane.showMessageDialog(this, "Insert Food Price");
                f1.setText("");
                f2.setText("");
                f3.setText("");
                f4.setText("");
                f5.setText("");
                
                p1.setText("");
                p2.setText("");
                p3.setText("");
                p4.setText("");
                p5.setText("");
            }

        }catch(Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(this, "Connection Failed!");
        }
        
        
    }

